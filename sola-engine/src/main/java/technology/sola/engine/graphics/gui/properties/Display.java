package technology.sola.engine.graphics.gui.properties;

/**
 * Display contains options for element rendering that change the layout.
 */
public enum Display {
  /**
   * Element will render and behave normally in the layout.
   */
  DEFAULT,
  /**
   * Element will not render and will not take up any space in the layout. Essentially, it will act is if it is not
   * there.
   */
  NONE,
}
