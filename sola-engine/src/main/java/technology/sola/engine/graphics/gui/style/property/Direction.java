package technology.sola.engine.graphics.gui.style.property;

/**
 * Direction defines the ways {@link technology.sola.engine.graphics.gui.GuiElement} children will flow.
 */
public enum Direction {
  /**
   * Children flow left to right.
   */
  ROW,
  /**
   * Children flow left to right in reverse order.
   */
  ROW_REVERSE,
  /**
   * Children flow from top to bottom.
   */
  COLUMN,
  /**
   * Children flow from top to bottom in reverse order.
   */
  COLUMN_REVERSE,
}
